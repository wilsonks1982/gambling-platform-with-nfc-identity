package com.wildace.roulette.config;


import com.wildace.roulette.websocket.WheelWebSocketHandler;
import org.springframework.context.annotation.Configuration;
import org.springframework.web.socket.config.annotation.EnableWebSocket;
import org.springframework.web.socket.config.annotation.WebSocketConfigurer;
import org.springframework.web.socket.config.annotation.WebSocketHandlerRegistry;

@Configuration
@EnableWebSocket
public class WebSocketConfig implements WebSocketConfigurer {

    private static final String GAME_WEBSOCKET_ENDPOINT = "/websocket/game";

    private final WheelWebSocketHandler wheelWebSocketHandler;

    public WebSocketConfig(WheelWebSocketHandler wheelWebSocketHandler) {
        this.wheelWebSocketHandler = wheelWebSocketHandler;
    }

    @Override
    public void registerWebSocketHandlers(WebSocketHandlerRegistry registry) {
        registry.addHandler(wheelWebSocketHandler, GAME_WEBSOCKET_ENDPOINT)
                .setAllowedOrigins("*");
    }

}